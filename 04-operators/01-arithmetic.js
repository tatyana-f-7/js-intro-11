// Simple ones + - * /

let num1 = 2;
let num2 = 5;
let num3 = 3;
let num4 = 6;

console.log(num4 / num1); // 3
console.log(num1 * num3 * num4); // 36
console.log(num3 - num4 - num1 + num4 * 2); // 7

console.log(num4 - (num3 - num1) * num2); // 1

// Exponentiation
console.log(3 ** 2); // 9
console.log(4 ** 3); // 64
console.log(5 ** 4); // 625

console.log(2 ** 5 / 4 + 3 * 2); // 14

// Modulus - Remainder
console.log(15 % 4); // 3

console.log(2 * 5 % 3 - 7); // -6
console.log(5 + 16 % 7 + 2); // 9

